package calculation

func Add(number int, numberTwo int) int {
	return tambah(number, numberTwo)
}

func tambah(number int, numberTwo int) int {
	return number + numberTwo
}

func Multiply(number int, numberTwo int) int {
	return kali(number, numberTwo)
}

func kali(number int, numberTwo int) int {
	return number * numberTwo
}
